DESCRIPTION :
======================

Ce programme a été créer pour le projet de bachelor Developpement Web et Mobilité de l'ECTEI. C'est un programme de gestion d'annonces pour véhicules (voitures, motos et scooters) avec possibilité d'ajout d'une annonce sous réserve de validation.
De plus, une gestion de membres est mise en place pour facilité l'inscription et la gestion.




